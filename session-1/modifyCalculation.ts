let n1: number = 2;

n1 = n1 + 5;
console.log(n1); // 7

n1 += 5; // n = n + 5
console.log(n1); // 12

// Increment dan decrement
let counter: number = 0;
console.log(counter++); // 1

console.log(counter--); // 0

let preCounter: number = 0;
console.log("Prefix : ", ++preCounter);

let postCounter: number = 0;
console.log("Postfix : ", postCounter++);
