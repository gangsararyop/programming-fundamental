var n1 = 2;
n1 = n1 + 5;
console.log(n1); // 7
n1 += 5; // n = n + 5
console.log(n1); // 12
// Increment dan decrement
var counter = 0;
console.log(counter++); // 1
console.log(counter--); // 0
var preCounter = 0;
console.log("Prefix : ", ++preCounter);
var postCounter = 0;
console.log("Postfix : ", postCounter++);
